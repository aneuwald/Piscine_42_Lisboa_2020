#include <stdlib.h>

int		*ft_range(int min, int max)
{
	int		i;
	int		size;
	int		*array;

	if (min >= max)
		return (0);
	size = max - min;
	if (!(array = malloc(sizeof(int) * size)))
		return (0);
	i = 0;
	while (min < max)
	{
		array[i] = min;
		i++;
		min++;
	}
	return (array);
}
