int		ft_sum(int a, int b)
{
	return (a + b);
}

int		ft_subtract(int a, int b)
{
	return (a - b);
}

int		ft_multiply(int a, int b)
{
	return (a * b);
}

int		ft_divide(int a, int b)
{
	return (a / b);
}

int		ft_rest(int a, int b)
{
	return (a % b);
}
